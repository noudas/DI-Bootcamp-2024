import turtle

turtle.speed(speed=2)
turtle.penup()
turtle.setpos(0,-300)
turtle.back(100)

turtle.pendown()
turtle.circle(100)
turtle.penup()
turtle.forward(200)

turtle.pendown()
turtle.circle(100)
turtle.left(90)
turtle.penup()
turtle.forward(200)
turtle.pendown()
turtle.forward(300)
turtle.right(90)
turtle.forward(100)
turtle.left(90)
turtle.circle(200,extent = 180)
turtle.left(90)
turtle.forward(100)
turtle.right(90)
turtle.forward(300)
turtle.done()